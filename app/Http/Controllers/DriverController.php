     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $newDriver = new Driver;
        $newDriver->driver_id = $request->driver["driver_id"];
        $newDriver->insurance_type_d = $request->driver["insurance_type_d"];
        $newDriver->driver_notes = $request->driver["driver_notes"];
        $newDriver->driver_phone_number = $request->driver["driver_phone_number"];
        $newDriver->driver_address = $request->driver["driver_address"];
        $newDriver->save();

        return $newDriver;
    }

